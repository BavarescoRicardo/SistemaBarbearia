package modelo;

import java.sql.Date;

/**
 *
 * @author Ricardo
 */
public class Agenda {
    private int id;
    private String nome;
    private String data;
    private String hora;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public Agenda() {
    }

    public Agenda(String nome, String data, String hora) {
        this.nome = nome;
        this.data = data;
        this.hora = hora;
    }
    
    
}
